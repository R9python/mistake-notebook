def get_knowledge_points_by_grade(grade_level):
    """获取指定年级的所有知识点"""
    points = []
    for category, grades in KNOWLEDGE_POINTS_DATA.items():
        if grade_level in grades:
            for item in grades[grade_level]:
                name, desc, semester, chapter = item
                points.append({
                    'name': name,
                    'category': category,
                    'description': desc,
                    'semester': semester,
                    'chapter': chapter,
                })
    return points


def get_all_knowledge_points():
    """获取所有知识点（用于AI分类）"""
    all_points = []
    for category, grades in KNOWLEDGE_POINTS_DATA.items():
        for grade_level, points in grades.items():
            for item in points:
                name, desc, semester, chapter = item
                all_points.append({
                    'name': name,
                    'category': category,
                    'grade_level': grade_level,
                    'description': desc,
                    'semester': semester,
                    'chapter': chapter,
                })
    return all_points
